package jfreechart;

import java.sql.*;
import java.util.*;

public class Database {

	String URL = "jdbc:oracle:thin:@127.0.0.1:1521:xe";
	String USER ="scott";
	String PASS = "tiger";

	public ArrayList<ArrayList> getData() {

		ArrayList<ArrayList> data = new ArrayList<ArrayList>();
		try{
			Class.forName("oracle.jdbc.driver.OracleDriver");
			Connection con = DriverManager.getConnection(URL, USER , PASS);	
			
			//***************************************************************
			String sql = "SELECT count(ename) AS worker, TO_char(hiredate,'mm') AS MONTH\r\n"
					+ "FROM EMP\r\n"
					+ "WHERE TO_CHAR(HIREDATE,'MM') IS NOT NULL \r\n"
					+ "GROUP BY TO_char(hiredate,'mm')\r\n"
					+ "ORDER BY TO_char(hiredate,'mm') ASC";
			//***************************************************************
			
			PreparedStatement stmt = con.prepareStatement( sql );	

			ResultSet rset = stmt.executeQuery();

			
			while( rset.next() ){
				ArrayList temp = new ArrayList();
				temp.add( rset.getInt("WORKER"));				//****************
				temp.add( rset.getString("MONTH"));		//****************		
				data.add(temp);
			}
			rset.close();
			stmt.close();
			con.close();
		} catch(Exception ex){
			System.out.println("에러 : " + ex.getMessage() );
		}
		return data;
	}
}
